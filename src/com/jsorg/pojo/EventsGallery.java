package com.jsorg.pojo;

public class EventsGallery {
	private String activityId;
	private String activityName;
	private String activityContent;
	private String headFigure;
	private String picture_list;
	private String author_id;
	private String time;
	private Boolean is_write;
	public String getActivityId() {
		return activityId;
	}
	public void setActivityId(String activityId) {
		this.activityId = activityId;
	}
	public String getActivityName() {
		return activityName;
	}
	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}
	public String getActivityContent() {
		return activityContent;
	}
	public void setActivityContent(String activityContent) {
		this.activityContent = activityContent;
	}
	public String getHeadFigure() {
		return headFigure;
	}
	public void setHeadFigure(String headFigure) {
		this.headFigure = headFigure;
	}
	public String getPicture_list() {
		return picture_list;
	}
	public void setPicture_list(String picture_list) {
		this.picture_list = picture_list;
	}
	public String getAuthor_id() {
		return author_id;
	}
	public void setAuthor_id(String author_id) {
		this.author_id = author_id;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public Boolean getIs_write() {
		return is_write;
	}
	public void setIs_write(Boolean is_write) {
		this.is_write = is_write;
	}
	
	
}
